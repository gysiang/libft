#include "libft.h"
#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	size_t		i;
	char		*s_char;

	s_char = (char *)s;
	i = 0;
	while (n > i)
	{
		s_char[i] = '\0';
		i++;
	}
}
